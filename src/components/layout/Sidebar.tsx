const getListDatabases = async () => {
  try {
    const result = await fetch(API_BASE_URL, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        NotionClientName: getClientAccountName(),
      },
      body: JSON.stringify({
        query: `{
            databases {
              object
              results {
                  id
                  title {
                      plain_text
                  }
                  description {
                      plain_text
                  }
                  object
                  properties
              }
          }
        }`,
      }),
    }).then((res) => res.json());
    return result.data.databases.results;
  } catch (error) {
    return [];
  }
};

